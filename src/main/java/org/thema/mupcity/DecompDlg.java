    private void previewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_previewButtonActionPerformed
        min = Double.parseDouble(minSizeTextField.getText());
        max = Double.parseDouble(maxSizeTextField.getText());
        exp = Integer.parseInt(expTextField.getText());
        AffineTransform trans = bounds.getTransform();
        double width = XAffineTransform.getScaleX0(trans);
        double height = XAffineTransform.getScaleY0(trans);
        double rot = XAffineTransform.getRotation(trans);
        MSGridBuilder<MSGrid> msGrid = new MSGridBuilder(JTS.geomFromRect(new Rectangle2D.Double(0, 0, width, height)), min, max, exp, 0, new SquareGridFactory());
        Envelope env = msGrid.getGrid(min).getEnvelope();

        AffineTransform newTrans = AffineTransform.getTranslateInstance(trans.getTranslateX(), trans.getTranslateY());
        newTrans.concatenate(AffineTransform.getScaleInstance(env.getWidth(), env.getHeight()));
        newTrans.concatenate(AffineTransform.getRotateInstance(rot));
        bounds.setTransform(newTrans);
        max = msGrid.getResolutions().first();
        maxSizeTextField.setText(""+max);
        if(bounds instanceof GridModShape)
            ((GridModShape)bounds).setGridSize(max);
        
        //bounds.setRect(JTS.getEnvelope2D(msGrid.getGrid(min).getEnvelope(), DefaultGeographicCRS.WGS84));
    }//GEN-LAST:event_previewButtonActionPerformed

